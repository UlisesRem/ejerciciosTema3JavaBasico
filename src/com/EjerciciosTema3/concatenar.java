package com.EjerciciosTema3;

public class concatenar {
    public static void main(String[] args) {
        conca();
    }

    static void conca(){
        String gatos [] = {"Coco", "Hayley", "Calixto"};

        for (int i=0;i<=0 ;i++){
            System.out.println(gatos[0]+" "+gatos[1]+" "+gatos[2]);
        }
    }
}